    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int index = tblSkill.getSelectedRow();
        UserSkill uskill = list.get(index);
        usdao.removeUserSkill(uskill.getId());
        fillWindow();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String skillName = txtSkillName.getText();
        Skill skill = null;
        if(skillName != null && !skillName.trim().isEmpty()){
            skill = new Skill(0,skillName);
            sdao.insertSkill(skill);
        }else {
            skill = (Skill) cbSkill.getSelectedItem();
        }
        int power = sliderPower.getValue();
        UserSkill uskill = new UserSkill(null, Config.loggedInUser, skill, power);
        usdao.insertUserSkill(uskill);
        fillWindow();
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       UserSkill selectedUserSkill = list.get(tblSkill.getSelectedRow());
       String skillName = txtSkillName.getText();
       Skill skill = null;
       if(skillName != null && !skillName.trim().isEmpty()){
            skill = new Skill(0,skillName);
            sdao.insertSkill(skill);
        }else {
            skill = (Skill) cbSkill.getSelectedItem();
        }
       int power = sliderPower.getValue();
       selectedUserSkill.setPower(power);
       selectedUserSkill.setSkill(skill);
       usdao.updateUserSkill(selectedUserSkill);
       fillWindow();
    }//GEN-LAST:event_btnSaveActionPerformed


