    public void fillWindow(){
        List<Skill> skills = sdao.getAllSkill();
        for(Skill s: skills){
            cbSkill.addItem(s);
        }
        fillTable();
    }
    
    List<UserSkill> list;
    
    public void fillTable(){
        list = usdao.getAllSkillsByUserId(Config.loggedInUser.getId());
        Vector<Vector> rows = new Vector<>();
        for(UserSkill us: list){
            Vector row = new Vector();
            row.add(us.getSkill());
            row.add(us.getPower());
            rows.add(row);
        }
        Vector<String> columns = new Vector<>();
        columns.add("Skill");
        columns.add("Power");
        DefaultTableModel model = new DefaultTableModel(rows,columns);
        tblSkill.setModel(model);
    }
    public void fillUserComponents(){
        fillWindow();
    }

